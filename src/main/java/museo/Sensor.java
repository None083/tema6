package museo;

/**
 *
 * @author noelia
 */
public abstract class Sensor {
    
    private String id;

    public Sensor(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Sensor{" + "id=" + id + '}';
    }
    
    public abstract void alarma(int valor);
    
}
